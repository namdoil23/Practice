package chap99_homework.homework04.b2;

public class Parent {
	
	String firstName;
	int height;
	String bloodType;
	String skincolor;
	
	public Parent()
	{
		
	}

	public Parent(String firstName, int height, String 
			bloodType, String skincolor) {
		this.firstName = firstName;
		this.height = height;
		this.bloodType = bloodType;
		this.skincolor = skincolor;
	}
	
	
}